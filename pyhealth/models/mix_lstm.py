"""
Mixture of Experts Long Short-Term Memory (MixLSTM) Model.

This module contains the implementation of the MixLSTM and its baseline comparison model.
The underlying Mixture of Experts (MoE) architecture and gating mechanisms are adapted 
from the original MixLSTM implementation.

Reference: https://gitlab.eecs.umich.edu/mld3/mlhc2019_relaxed_parameter_sharing
           https://arxiv.org/abs/1906.02898
"""

from typing import Optional, Tuple, Callable, List, Union
import math
import torch
import torch.nn as nn
from collections import abc
from pyhealth.models import BaseModel

###########################################################################
# Part 1: Mixture of Experts (MoE) Base Classes & Gating Mechanisms
###########################################################################
class MoE(nn.Module):
    """Abstract base class for Mixture of Experts (MoE).
    
    This class supports specifying the expert networks and the gating function.
    It requires combining functions (implemented in subclasses like MoO or MoW).

    Args:
        experts (Union[nn.ModuleList, nn.Module]): The expert models.
        gate (nn.Module): The gating function/network.
    """
    def __init__(self, experts, gate):
        super(MoE, self).__init__()        
        self.experts = experts
        self.gate = gate

class MoO(MoE):
    """Mixture of Outputs (MoO) module.
    
    Combines the outputs of multiple experts using the coefficients 
    generated by the gating function.

    Args:
        experts (nn.ModuleList): A list of expert models.
        gate (nn.Module): The gating function/network.
        bs_dim (int, optional): The batch size dimension index. Defaults to 1 for RNNs.
        expert_dim (int, optional): The expert dimension index. Defaults to 0.
    """
    def __init__(self, experts, gate, bs_dim=1, expert_dim=0):
        super(MoO, self).__init__(experts, gate)        
        self.bs_dim = bs_dim
        self.expert_dim = expert_dim

    def combine(self, o: Union[List[torch.Tensor], Tuple], coef: torch.Tensor) -> torch.Tensor:
        """Combines expert outputs based on gating coefficients."""
        if isinstance(o[0], abc.Sequence): 
            return [self.combine(o_, coef) for o_ in zip(*o)]
        else:
            o = torch.stack(o)
            o = o.transpose(self.expert_dim, -1)
            o = o.transpose(self.bs_dim, -2) 

            res = o * coef
            res = res.transpose(self.expert_dim, -1)
            res = res.transpose(self.bs_dim, -2)
            return res.sum(0)
        
    def forward(self, x: torch.Tensor, coef: Optional[torch.Tensor] = None) -> torch.Tensor:
        """Forward pass for Mixture of Outputs."""
        coef = self.gate(x, coef)
        self.last_coef = coef
        o = [expert(x) for expert in self.experts]
        return self.combine(o, coef)

class MoW(MoE):
    """Mixture of Weights (MoW) module."""
    def forward(self, x: torch.Tensor, coef: Optional[torch.Tensor] = None) -> torch.Tensor:
        """Forward pass for Mixture of Weights.""" 
        coef = self.gate(x, coef)
        self.last_coef = coef
        return self.experts(x, coef)

class Gate(nn.Module):
    """Abstract base class for gate functions in the Mixture of Experts."""
    def forward(self, x: torch.Tensor, coef: Optional[torch.Tensor] = None) -> torch.Tensor:
        raise NotImplementedError()

class NonAdaptiveGate(Gate):
    """Gate with fixed or learnable non-adaptive coefficients."""
    def __init__(self, num_experts, coef=None, fixed=False, normalize=False):
        super(self.__class__, self).__init__()
        self.normalize = normalize
        if coef is None: # initialization
            coef = torch.ones(num_experts)
            nn.init.uniform_(coef)
        if fixed:
            coef = nn.Parameter(coef, requires_grad=False)
        else:
            coef = nn.Parameter(coef)

        self.coefficients = coef

    def forward(self, x: torch.Tensor, coef: Optional[torch.Tensor] = None) -> torch.Tensor:
        """Computes the gating coefficients."""
        if self.normalize:
            return nn.functional.softmax(self.coefficients, 0)
        else:
            return self.coefficients
        
class IDGate(Gate): 
    """Identity gate that simply passes through the previous coefficients."""
    def forward(self, x: torch.Tensor, coef: torch.Tensor) -> torch.Tensor:
        return coef

def moo_linear(in_features, out_features, num_experts, bs_dim=1, expert_dim=0):
    """Creates a Mixture of Outputs (MoO) linear layer."""
    experts = nn.ModuleList()
    for _ in range(num_experts):
        experts.append(nn.Linear(in_features, out_features))   
    return MoO(experts, IDGate(), bs_dim=bs_dim, expert_dim=expert_dim) 

