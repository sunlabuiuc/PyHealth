class MixLSTM(BaseModel):
    """Mixture of Experts Long Short-Term Memory (MixLSTM) model.
    
    This model implements the MixLSTM architecture for clinical time-series prediction,
    utilizing a mixture of experts with gating mechanisms to capture complex temporal dynamics,
    particularly suited for irregular and imbalanced clinical data.
    
    Args:
        dataset: The PyHealth dataset object.
        input_size (int): The dimensionality of the input features. Defaults to 76.
        hidden_size (int): The number of features in the hidden state. Defaults to 128.
        num_experts (int): The number of expert LSTM cells. Defaults to 2.
        max_seq_len (int): The maximum sequence length for target replication. Defaults to 48.
        **kwargs: Additional keyword arguments.
    Example:
        >>> from pyhealth.datasets import SampleEHRDataset
        >>> dataset = SampleEHRDataset()
        >>> model = MixLSTM(dataset=dataset, input_size=76, hidden_size=128)
        >>> # Assuming 'data_batch' is obtained from a PyHealth DataLoader
        >>> res = model(**data_batch)
        >>> print(res["y_prob"].shape)
    """
    def __init__(self, dataset, input_size=76,hidden_size=128, num_experts=2, max_seq_len=48, **kwargs):
        super().__init__(dataset=dataset)
        
        if hasattr(dataset, "input_schema") and "physio_features" in dataset.input_schema:
            self.input_size = dataset.input_schema["physio_features"].dim
        else:
            self.input_size = input_size
        self.output_size = self.get_output_size()
        
        self.hidden_size = hidden_size
        self.num_experts = num_experts
        self.max_seq_len = max_seq_len
        
        self.model = ExampleMowLSTM(
            input_size=self.input_size, 
            hidden_size=self.hidden_size,
            num_classes=self.output_size, 
            num_layers=1,
            num_directions=1,
            dropout=0,
            activation=None
        )
        
        self.model.set_kt(self.num_experts, self.max_seq_len)

    def forward(self, **kwargs) -> dict:
        """Forward pass conforming to PyHealth BaseModel standards."""
        x = kwargs[self.feature_keys[0]]
        batch_size = x.size(0)
        seq_len = x.size(1)
        
        x_permuted = x.permute(1, 0, 2)
        input_lengths = [seq_len] * batch_size 

        h = torch.zeros(1, batch_size, self.hidden_size, device=self.device)
        c = torch.zeros(1, batch_size, self.hidden_size, device=self.device)
        states = (h, c)
        
        outputs, _ = self.model(x_permuted, states, input_lengths)
        logits_seq = outputs.permute(1, 0, 2)

        res = {}

        if self.label_keys[0] in kwargs:
            y_true = kwargs[self.label_keys[0]]
            y_true_replicated = y_true.unsqueeze(1).repeat(1, seq_len, 1)
            loss_fn = self.get_loss_function()

            loss = loss_fn(
                logits_seq.reshape(-1, self.output_size), 
                y_true_replicated.reshape(-1, 1).float() if self.mode == "binary" else y_true_replicated.view(-1)
            )
            res["loss"] = loss
            res["y_true"] = y_true

        logits_max, _ = torch.max(logits_seq, dim=1) 
        y_prob_max = self.prepare_y_prob(logits_max) 
        
        res["logit"] = logits_max
        res["y_prob"] = y_prob_max
            
        return res

