"""PyHealth dataset for composite dermoscopic image classification.

Combines dermoscopy datasets — ISIC 2018, HAM10000, and PH2 (and synthetic subsets) — into
a single unified dataset for binary melanoma classification. Each dataset
provides dermoscopic images with segmentation masks, enabling mode-based
processing (whole image, lesion only, or background only).

Data Sources:
    ISIC 2018:
        - Part of the ISIC Skin Lesion Analysis challenge
        - JPG images with PNG segmentation masks
        - Dataset is available at
            for images + metadata
            https://api.isic-archive.com/collections/63/
            for segmentation masks (Task 1 Training Ground Truth)
            https://challenge.isic-archive.com/data/#2018
        - Codella et al., "Skin Lesion Analysis Toward Melanoma Detection" (2018)
    HAM10000:
        - "Human Against Machine with 10000 training images"
        - JPG images with PNG segmentation masks
        - Dataset is available at
            for images + metadata
            https://api.isic-archive.com/collections/66/
            for segmentation masks
            https://www.kaggle.com/datasets/tschandl/ham10000-lesion-segmentations
        - Tschandl et al., "The HAM10000 dataset, a large collection of multi-source dermatoscopic images of common pigmented skin lesions" (2018)
    PH2:
        - Dermoscopic Image Database from Pedro Hispano hospital
        - BMP images with BMP segmentation masks in nested directories
        - Dataset is available at
            for images + metadata + segmentation masks
            https://www.kaggle.com/datasets/spacesurfer/ph2-dataset
        - Mendonça et al., "PH2 - A dermoscopic image database for research and benchmarking" (2013)

Author:
    Generated for PyHealth integration of dermoscopic_artifacts datasets.
"""

import logging
import os
from functools import wraps
from pathlib import Path
from typing import Dict, List, Optional, Any

import pandas as pd

from pyhealth.datasets.base_dataset import BaseDataset

from pyhealth.processors import DermoscopyImageProcessor
from pyhealth.tasks import DermoscopyMelanomaClassification

logger = logging.getLogger(__name__)

class DermoscopyDataset(BaseDataset):
    """Composite dermoscopy dataset combining ISIC 2018, HAM10000, and PH2.

    This dataset unifies three dermoscopic image datasets into a single
    PyHealth-compatible dataset for binary melanoma classification. Each
    sample includes an image path, a segmentation mask path, a binary label,
    and a source dataset identifier.

    The dataset expects a root directory containing sub-dataset folders and
    external metadata CSV files that describe the images and labels.

    Args:
        dermoscopy_data/
        ├── dermoscopy-metadata-pyhealth.csv  # ← generated by prepare_metadata()
        ├── ham10000/
        │   ├── images/                     # ISIC_*.jpg  (10 015 files)
        │   ├── masks/                      # ISIC_*_segmentation.png  (10 015 files)
        │   └── images/metadata.csv         # raw — columns: isic_id, diagnosis_1, ...
        ├── isic2018/
        │   ├── images/                     # ISIC_*.jpg  (2 594 files)
        │   ├── masks/                      # ISIC_*_segmentation.png  (2 594 files)
        │   └── images/metadata.csv         # raw — columns: isic_id, diagnosis_1, ...
        └── ph2/
            ├── PH2 Dataset images/         # original nested BMP structure
            │   ├── IMD002/
            │   │   ├── IMD002_Dermoscopic_Image/IMD002.bmp
            │   │   └── IMD002_lesion/IMD002_lesion.bmp
            │   └── ...
            └── PH2_dataset.txt             # raw pipe-delimited metadata

        ```
            **Label encoding:**
            - 0 = benign
            - 1 = melanoma
            - e.g., HAM10000 `'Indeterminate'` cases are dropped (130 out of 10 015)

        datasets: List of sub-datasets to include. Any subset of
            ["isic2018", "ham10000", "ph2"]. Defaults to all three.
        dataset_name: Optional name for the dataset. Defaults to "dermoscopy".
        config_path: Optional path to YAML config. Defaults to built-in config.
        cache_dir: Optional directory for caching processed data.
        num_workers: Number of parallel workers. Defaults to 1.
        dev: If True, loads a small subset for development. Defaults to False.

    Attributes:
        root: Root directory of the raw data.
        dataset_name: Name of the dataset.
        available_datasets: List of sub-datasets included.

    Examples:
        >>> from pyhealth.datasets import DermoscopyDataset
        >>> dataset = DermoscopyDataset(
        ...     root="/path/to/dermoscopy_data",
        ... )
        >>> dataset.stats()
        >>> samples = dataset.set_task()
        >>> print(samples[0])
    """

    SUPPORTED_DATASETS = ("isic2018", "ham10000", "ph2")

    def __init__(
        self,
        root: str,
        datasets: Optional[List[str]] = None,
        isic2018_metadata_path: Optional[str] = None,
        ham10000_metadata_path: Optional[str] = None,
        ph2_metadata_path: Optional[str] = None,
        dataset_name: Optional[str] = None,
        config_path: Optional[str] = None,
        cache_dir: Optional[str] = None,
        num_workers: int = 4,
        dev: bool = False,
    ) -> None:
        # PATCH 1: Keeps default logic, but removes the strict ValueError 
        # that prevents loading synthetic Trap Sets like 'ph2_with_ruler'.
        if datasets is None:
            self.available_datasets = list(self.SUPPORTED_DATASETS)
        else:
            self.available_datasets = datasets

        self._metadata_paths = {
            "isic2018": isic2018_metadata_path,
            "ham10000": ham10000_metadata_path,
            "ph2": ph2_metadata_path,
        }

        if config_path is None:
            logger.info("No config path provided, using default config")
            config_path = Path(__file__).parent / "configs" / "dermoscopy.yaml"

        self.prepare_metadata(root)

        default_tables = ["dermoscopy"]
        super().__init__(
            root=root,
            tables=default_tables,
            dataset_name=dataset_name or "dermoscopy",
            config_path=config_path,
            cache_dir=cache_dir,
            num_workers=num_workers,
            dev=dev,
        )

    def prepare_metadata(self, root: str) -> None:
        """Prepare unified metadata CSV from all enabled sub-datasets.

        Reads each sub-dataset's metadata, normalizes column names and paths,
        then concatenates everything into a single CSV with the schema:
            patient_id, image_path, mask_path, label, source_dataset

        Args:
            root: Root directory containing sub-dataset folders.
        """
        all_frames = []

        # PATCH 2: Loop through dynamic datasets instead of hardcoding 3 IF statements.
        for ds in self.available_datasets:
            if ds in ["isic2018", "ham10000"]:
                df = self._prepare_isic_ham(root, ds)
            elif ds == "ph2":
                df = self._prepare_ph2(root)
            elif ds.startswith("ph2_with_"):
                # Route synthetic datasets to our new helper function
                df = self._prepare_trap_set(root, ds) 
            else:
                df = None
                
            if df is not None:
                all_frames.append(df)

        if not all_frames:
            raise ValueError(
                "No metadata could be loaded. Provide at least one metadata "
                "CSV path for the enabled datasets."
            )

        combined = pd.concat(all_frames, axis=0, ignore_index=True)
        # Ensure patient_id exists for PyHealth engine
        if 'patient_id' not in combined.columns:
            combined['patient_id'] = combined['isic_id'] if 'isic_id' in combined.columns else combined.index.astype(str)
            combined['visit_id'] = combined['patient_id']

        output_path = os.path.join(root, "dermoscopy-metadata-pyhealth.csv")
        combined.to_csv(output_path, index=False)
        logger.info(
            f"Saved combined metadata ({len(combined)} samples) to {output_path}"
        )

    # =========================================================================
    # PATCH 3: Add one new helper function for the Stable Diffusion data
    # =========================================================================
    def _prepare_trap_set(self, root: str, dataset: str) -> Optional[pd.DataFrame]:
        """Prepare metadata for synthetically generated Trap Sets."""
        csv_path = os.path.join(root, dataset, "images", "metadata.csv")
        if not os.path.exists(csv_path): return None
        
        df = pd.read_csv(csv_path)
        id_col = 'isic_id' if 'isic_id' in df.columns else 'image_id'
        img_dir = os.path.join(root, dataset, "images")
        
        df['image_path'] = df[id_col].apply(lambda x: os.path.join(img_dir, f"{x}.jpg"))
        df['label'] = df['diagnosis_1'].apply(lambda x: 1 if str(x).strip().lower() == 'malignant' else 0)
        df['mask_path'] = "" 
        df['source_dataset'] = dataset
        return df

    def _filter_labels(self, csv_path: str, dataset: str) -> Optional[pd.DataFrame]:
        """Filter out samples with non-binary labels.

        For example, HAM10000 has an "Indeterminate" class that we exclude.

        Args:
            root: Root directory containing sub-dataset folders.
            dataset: Name of the dataset to filter.

        Returns:
            Filtered DataFrame containing only rows where label is 0 or 1.
        """
        df = pd.read_csv(csv_path)
        all_length = len(df)
        df["isic_id"] = df["isic_id"] + ".jpg"
        df["label"] = (df["diagnosis_1"] == "Malignant").astype(int)

        df = df[df["diagnosis_1"].isin(["Benign", "Malignant"])][["isic_id", "label"]]
        filtered_length = len(df)

        logger.info(
            f"{dataset}: Filtered to binary labels only. {filtered_length} samples remain "
            f"(dropped {all_length - filtered_length} indeterminate cases)"
        )
        return df

    def _append_sub_metadata(
        self, root: str, df: pd.DataFrame, dataset: str
    ) -> List[Dict[str, Any]]:
        """Append dataset-specific metadata columns to the DataFrame.

        For example, we can add a "source_dataset" column to identify which
        sub-dataset each sample came from.

        Args:
            root: Root directory containing sub-dataset folders.
            df: DataFrame containing the unified metadata.
            dataset: Name of the dataset to append.
        Returns:
            DataFrame with additional columns for the specified dataset.
        """
        image_dir = os.path.join(root, dataset, "images")
        mask_dir = os.path.join(root, dataset, "masks")

        records = []
        for _, row in df.iterrows():
            img_name = row["isic_id"]
            label = int(row["label"])
            image_path = os.path.join(image_dir, img_name)
            # Derive mask path: ISIC_XXXX.JPG -> ISIC_XXXX_segmentation.png
            mask_name = img_name.replace(".jpg", "_segmentation.png")
            mask_path = os.path.join(mask_dir, mask_name)
            patient_id = f"isic_{Path(img_name).stem}"
            records.append(
                {
                    "patient_id": patient_id,
                    "image_path": image_path,
                    "mask_path": mask_path,
                    "label": label,
                    "source_dataset": dataset,
                }
            )
        return records

    def _prepare_isic_ham(self, root: str, dataset: str) -> Optional[pd.DataFrame]:
        """Prepare ISIC 2018 sub-dataset metadata.

        Expects the metadata CSV to have columns: "image" (filename), "label" (0/1).
        Images are JPG files; masks are PNG segmentation files.
        """
        csv_path = os.path.join(root, dataset, "images", "metadata.csv")
        if not os.path.exists(csv_path):
            logger.warning(f"{dataset} raw metadata not found, skipping.")
            return None

        df = self._filter_labels(csv_path, dataset)

        result = pd.DataFrame(self._append_sub_metadata(root, df, dataset))
        logger.info(f"Prepared {dataset}: {len(result)} samples")
        return result

    def _prepare_ph2(self, root: str) -> Optional[pd.DataFrame]:
        """Prepare PH2 sub-dataset metadata.

        Expects the metadata CSV to have columns: "Name" (folder name), "label" (0/1).
        Images follow nested BMP structure:
            {root}/ph2/PH2 Dataset images//{Name}/{Name}_Dermoscopic_Image/{Name}.bmp
        Masks:
            {root}/ph2/PH2 Dataset images/{Name}/{Name}_lesion/{Name}_lesion.bmp
        """
        ph2_txt = os.path.join(root, "ph2", "PH2_dataset.txt")
        ph2_data_dir = os.path.join(root, "ph2", "PH2 Dataset images")
        records = []
        if os.path.exists(ph2_txt):
            with open(ph2_txt) as f:
                for line in f:
                    parts = [p.strip() for p in line.split("||")]
                    if len(parts) >= 4 and parts[1].startswith("IMD"):
                        name = parts[1].strip()
                        try:
                            clinical = int(parts[3].strip())
                        except ValueError:
                            continue
                        label = 1 if clinical == 2 else 0
                        image_path = os.path.join(
                            ph2_data_dir,
                            name,
                            f"{name}_Dermoscopic_Image",
                            f"{name}.bmp",
                        )
                        mask_path = os.path.join(
                            ph2_data_dir, name, f"{name}_lesion", f"{name}_lesion.bmp"
                        )
                        patient_id = f"ph2_{name}"
                        records.append(
                            {
                                "patient_id": patient_id,
                                "image_path": image_path,
                                "mask_path": mask_path,
                                "label": label,
                                "source_dataset": "ph2",
                            }
                        )
        else:
            print(f"WARNING: PH2_dataset.txt not found at {ph2_txt}")
        result = pd.DataFrame(records)
        logger.info(f"Prepared PH2: {len(result)} samples")
        return result

    @property
    def default_task(self) -> DermoscopyMelanomaClassification:
        """Returns the default task for this dataset.

        Returns:
            DermoscopyMelanomaClassification: Binary melanoma classification task.
        """
        return DermoscopyMelanomaClassification()

    @wraps(BaseDataset.set_task)
    def set_task(self, *args, **kwargs):
        """Override to inject default DermoscopyImageProcessor if none provided.

        If no input processor is specified for the "image" key, a default
        DermoscopyImageProcessor with mode="whole" is injected.
        """
        input_processors = kwargs.get("input_processors", None)
        if input_processors is None:
            input_processors = {}
        if "image" not in input_processors:
            input_processors["image"] = DermoscopyImageProcessor(mode="whole")
        kwargs["input_processors"] = input_processors
        return super().set_task(*args, **kwargs)