from __future__ import annotations

import logging
from pathlib import Path
from typing import List, Optional

import pandas as pd
from pyhealth.datasets import BaseDataset

logger = logging.getLogger(__name__)


class SEERDataset(BaseDataset):
    """SEER breast cancer tabular dataset for survival prediction.

    This class is responsible for loading and managing the preprocessed SEER dataset.
    It acts as a PyHealth wrapper around the tabular CSV generated by the 
    preprocessing pipeline.

    During initialization, `prepare_metadata` will:
      1. Read the input CSV (`root/processed/seer_ml_ready.csv`).
      2. Add synthetic `patient_id` and `visit_id` if missing.
      3. Add a synthetic `event_time` column derived from `year_dx`.
      4. Write a PyHealth-friendly CSV (`root/processed/seer_pyhealth.csv`).
    
    It then initializes `BaseDataset` using the static configuration schema located
    at `pyhealth/datasets/configs/seer.yaml`.

    The SEER (Surveillance, Epidemiology, and End Results) registry contains
    demographic, clinical, and survival data for cancer patients. Because it is a
    private dataset, access to the raw data requires authorization via the official
    SEER program and SEER*Stat software.

    Dataset is available for authorized researchers from:
        - Official SEER Program: https://seer.cancer.gov/data/access.html

    Reference Implementation:
        - GitHub: https://github.com/stefanhgm/MLHC2018-reproducible-survival-seer

    Citation:
        Hegselmann S, Greulich L, Varghese J, Dugas M. Reproducible Survival Prediction
        with SEER Cancer Data. Proceedings of Machine Learning Research: Machine
        Learning for Healthcare. 2018;85:1-26.

    Notes:
        - Each row in the source CSV is treated as exactly one patient and one visit.
        - The associated Task class decides which columns are input features and
          which are labels.
        - This dataset class does NOT rebuild or filter labels; it strictly exposes
          the preprocessed table to PyHealth.

    Args:
        root (str): The root directory where the preprocessed dataset CSV is stored.
        tables (Optional[List[str]]): A list of tables to be included. 
            Defaults to ["seer"].
        dataset_name (Optional[str]): The name of the dataset. Defaults to "seer".
        config_path (Optional[str]): The path to the configuration file. Defaults to 
            `configs/seer.yaml`.
        dev (bool): Whether to enable dev mode (limits to a subset of patients).
            Defaults to False.
        cache_dir (Optional[str]): Directory to store cached data.
        **kwargs: Additional arguments passed to BaseDataset.

    Examples:
        >>> from pyhealth.datasets import SEERDataset
        >>> # Load SEER dataset using the preprocessed PyHealth-ready table
        >>> dataset = SEERDataset(
        ...     root="/path/to/processed",
        ...     tables=["seer"],
        ... )
        >>> dataset.stats()

    Attributes:
        root (str): The root directory where the dataset is stored.
        tables (List[str]): A list of tables to be included in the dataset.
        dataset_name (str): The name of the dataset.
        config_path (str): The path to the configuration file.
    """

    def __init__(
        self,
        root: str,
        tables: Optional[List[str]] = None,
        dataset_name: Optional[str] = None,
        config_path: Optional[str] = None,
        **kwargs
    ) -> None:

        self.root = Path(root)

        self.prepare_metadata(self.root)

        if config_path is None:
            logger.info("No config path provided, using default config")
            config_path = Path(__file__).parent / "configs" / "seer.yaml"

        if tables is None:
            tables = ["seer"]

        super().__init__(
            root=str(self.root),
            tables=tables,
            dataset_name=dataset_name or "seer",
            config_path=str(config_path),
            **kwargs
        )

    def prepare_metadata(self, root: Path) -> None:
        """Prepare a PyHealth-compatible CSV from the ML-ready output.
        
        Will be called in `self.__init__()` during dataset initialization.
        
        Args:
            root (Path): Project root directory containing the processed data.
            
        Returns:
            None: This function writes the modified CSV to disk and returns nothing.
        """
        processed_dir = root / "processed"
        processed_dir.mkdir(parents=True, exist_ok=True)

        source_csv = processed_dir / "seer_ml_ready.csv"
        output_csv = processed_dir / "seer_pyhealth.csv"

        if not source_csv.exists() and output_csv.exists():
            source_csv = output_csv

        if not source_csv.exists():
            return

        df = pd.read_csv(source_csv)

        if "patient_id" not in df.columns:
            df.insert(0, "patient_id", [f"seer_{i}" for i in range(len(df))])

        if "visit_id" not in df.columns:
            df.insert(1, "visit_id", [f"visit_{i}" for i in range(len(df))])

        if "event_time" not in df.columns:
            if "year_dx" in df.columns:
                year_series = (
                    pd.to_numeric(df["year_dx"], errors="coerce")
                    .fillna(2000)
                    .astype(int)
                )
                df.insert(2, "event_time", year_series.astype(str) + "-01-01")
            else:
                df.insert(2, "event_time", "2000-01-01")

        df.to_csv(output_csv, index=False)
        logger.info("Generated PyHealth SEER table at %s", output_csv)

    @staticmethod
    def info() -> None:
        """Prints the expected input format for the SEER dataset.
        
        Can be called independently by the user to check dataset requirements.
        
        Args:
            None
            
        Returns:
            None: Prints the format requirements to standard output.
        """
        print(
            "SEERDataset relies on pyhealth/datasets/configs/seer.yaml, which expects\n"
            "a preprocessed PyHealth-ready CSV named:\n"
            "  seer_pyhealth.csv\n\n"
            "If missing, it will attempt to generate it from:\n"
            "  seer_ml_ready.csv\n\n"
            "Required columns in the CSV:\n"
            "  - age\n"
            "  - year_dx\n"
            "  - label\n"
            "  - one-hot feature columns (race_*, grade_*, stage_*, histology_*, etc.)"
            "\n\n"
            "The dataset wrapper will auto-create if missing:\n"
            "  - patient_id\n"
            "  - visit_id\n"
            "  - event_time\n\n"
            "The corresponding seer.yaml configuration must be present in the\n"
            "pyhealth/datasets/configs/ directory."
        )